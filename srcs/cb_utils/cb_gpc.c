#include "cube.h"

int	cb_gpc(t_img *img, int x, int y)
{
	if (x > img->sx || x < 0 || y > img->sy || y < 0)
		return (0);
	return (*(int *)(img->addr + (y * img->line_len + x * (img->bpp / 8))));
}
