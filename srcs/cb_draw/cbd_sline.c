#include "cube.h"

void	cbd_sline(int color, int width, int s[2], int e[2])
{
	if (e[0] < 0 || e[1] < 0 || e[0] > W_WIDE || e[1] > W_HIGH)
		return ;
	cbd_line(color, width, s, e);
}
